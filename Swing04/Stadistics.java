package swing03;
class Stadistics {
    private int lives;
    private int points;
    
    public Stadistics(int lives, int points) {
        this.lives = lives; this.points = points;
    }
    
    public int getLives() {
        return lives;
    }
    
    public int getPoints() {
        return points;
    }
}